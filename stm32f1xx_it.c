void TIM1_UP_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_IRQn 0 */
	if(w%50000==0 && scenePointer > 0)
	{	
	if(input[i] > password[i])
	{
		printf("Timeleft : %d sec ||| CurrentNumber : %d ||| Smaller!!!\n\r", Tlimit, input[i]);
	}
	else if(input[i] < password[i])
	{
		printf("Timeleft : %d sec ||| CurrentNumber : %d ||| Bigger!!!\n\r", Tlimit, input[i]);
	}

	Tlimit--;
	w=0;
	}
	w++;
  /* USER CODE END TIM1_UP_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_IRQn 1 */

  /* USER CODE END TIM1_UP_IRQn 1 */
}
